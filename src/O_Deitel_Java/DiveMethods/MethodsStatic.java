package O_Deitel_Java.DiveMethods;

public class MethodsStatic {
	public static void main(String[] args) {
		// Math class methods
		System.out.printf("%.2f", Math.sqrt(500));
		
		System.out.println();

		System.out.println(Math.max(6, 2));

		System.out.println(Math.pow(2, 2));

		System.out.printf("%.2f", Math.log(100));

	}
}
